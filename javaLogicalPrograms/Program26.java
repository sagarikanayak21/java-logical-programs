// Display the elements of an Integer array

public class Program26 {
    public static void main(String[] args) {
        Integer[] a = new Integer[5];
        a[0] = 60;
        a[1] = 70;
        a[2] = 10;
        a[3] = 50;
        a[4] = 80;
        for(int i=0; i<a.length; i++){
            System.out.println(a[i]);
        }
    }
}
